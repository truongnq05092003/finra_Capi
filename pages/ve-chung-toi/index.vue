    let vSwiperRef: any = null;
    const vSwiperIndex = ref<number>(0);

    const setVSwiperRef = (swiper: any) => {
      vSwiperRef = swiper;
    };

    const handleChangeSlide = (index: number) => {
      vSwiperRef?.slideTo(index);
    };

    const updateVSwiperIndex = () => {
      return Array.from(
        { length: vSwiperRef?.slides?.length || 0 },
        (_, index) => index === vSwiperRef?.activeIndex
      );
    };

    const dots = computed(() => {
      return Array.from(
        { length: vSwiperRef?.slides?.length || 0 },
        (_, index) => index === vSwiperRef?.activeIndex
      );
    });

    return {
      vSwiperRef,
      vSwiperIndex,
      SwiperAutoplay,
      SwiperPagination,
      setVSwiperRef,
      updateVSwiperIndex,
      handleChangeSlide,
      dots,
    };
  },
});
